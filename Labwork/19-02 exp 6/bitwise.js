let a=5&2  //AND
document.writeln("5 & 2 ;= ",a,"</br>")

let b=5|2  //OR
document.writeln("5 | 2 ;= ",b,"</br>")

let c=~5 //NOT
document.writeln("~5 ;= ",c,"</br>")

let d=5^1  //XOR
document.writeln("5^1 ;= ",d,"</br>")

let e=5<<1  //Zero fill left shift
document.write("5<<1 ;= ",e,"</br>")

let f=5>>1  //Signed right shift
document.write("5>>1 ;= ",f,"</br>")

let g=5>>>1  //zero fill right shift
document.write("5>>>1 ;= ",g,"</br>")